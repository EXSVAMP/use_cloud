var arrayMap = require('lib/lodash/_arrayMap'),
    baseIteratee = require('lib/lodash/_baseIteratee'),
    baseMap = require('lib/lodash/_baseMap'),
    baseSortBy = require('lib/lodash/_baseSortBy'),
    baseUnary = require('lib/lodash/_baseUnary'),
    compareMultiple = require('lib/lodash/_compareMultiple'),
    identity = require('lib/lodash/identity');

/**
 * The base implementation of `_.orderBy` without param guards.
 *
 * @private
 * @param {Array|Object} collection The collection to iterate over.
 * @param {Function[]|Object[]|string[]} iteratees The iteratees to sort by.
 * @param {string[]} orders The sort orders of `iteratees`.
 * @returns {Array} Returns the new sorted array.
 */
function baseOrderBy(collection, iteratees, orders) {
  var index = -1;
  iteratees = arrayMap(iteratees.length ? iteratees : [identity], baseUnary(baseIteratee));

  var result = baseMap(collection, function(value, key, collection) {
    var criteria = arrayMap(iteratees, function(iteratee) {
      return iteratee(value);
    });
    return { 'criteria': criteria, 'index': ++index, 'value': value };
  });

  return baseSortBy(result, function(object, other) {
    return compareMultiple(object, other, orders);
  });
}

module.exports = baseOrderBy;
