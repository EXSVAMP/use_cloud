var Symbol = require('lib/lodash/_Symbol'),
    copyArray = require('lib/lodash/_copyArray'),
    getTag = require('lib/lodash/_getTag'),
    isArrayLike = require('lib/lodash/isArrayLike'),
    isString = require('lib/lodash/isString'),
    iteratorToArray = require('lib/lodash/_iteratorToArray'),
    mapToArray = require('lib/lodash/_mapToArray'),
    setToArray = require('lib/lodash/_setToArray'),
    stringToArray = require('lib/lodash/_stringToArray'),
    values = require('lib/lodash/values');

/** `Object#toString` result references. */
var mapTag = '[object Map]',
    setTag = '[object Set]';

/** Built-in value references. */
var iteratorSymbol = typeof (iteratorSymbol = Symbol && Symbol.iterator) == 'symbol' ? iteratorSymbol : undefined;

/**
 * Converts `value` to an array.
 *
 * @static
 * @since 0.1.0
 * @memberOf _
 * @category Lang
 * @param {*} value The value to convert.
 * @returns {Array} Returns the converted array.
 * @example
 *
 * _.toArray({ 'a': 1, 'b': 2 });
 * // => [1, 2]
 *
 * _.toArray('abc');
 * // => ['a', 'b', 'c']
 *
 * _.toArray(1);
 * // => []
 *
 * _.toArray(null);
 * // => []
 */
function toArray(value) {
  if (!value) {
    return [];
  }
  if (isArrayLike(value)) {
    return isString(value) ? stringToArray(value) : copyArray(value);
  }
  if (iteratorSymbol && value[iteratorSymbol]) {
    return iteratorToArray(value[iteratorSymbol]());
  }
  var tag = getTag(value),
      func = tag == mapTag ? mapToArray : (tag == setTag ? setToArray : values);

  return func(value);
}

module.exports = toArray;
