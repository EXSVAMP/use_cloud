var Hash = require('lib/lodash/_Hash'),
    ListCache = require('lib/lodash/_ListCache'),
    Map = require('lib/lodash/_Map');

/**
 * Removes all key-value entries from the map.
 *
 * @private
 * @name clear
 * @memberOf MapCache
 */
function mapCacheClear() {
  this.__data__ = {
    'hash': new Hash,
    'map': new (Map || ListCache),
    'string': new Hash
  };
}

module.exports = mapCacheClear;
